#include "3-calc.h"

/**
 * op_add - Function that sum to integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: result of sum of two integers.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function that subtract to integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: result of substraction of two integers.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function that multiplies to integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: result of multiplication of two integers.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function that divide to integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: result of division of two integers.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Function that give the module of integers.
 * @a: first integer.
 * @b: second integer.
 *
 * Return: result of module of two integers.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
